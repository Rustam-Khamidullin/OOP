package ru.nsu.khamidullin;

/**
 * The {@code Subject} enum represents the academic subjects for a student's credit book.
 * It includes values for OSI (Operating Systems), OOP (Object-Oriented Programming),
 * DUITFCP (Data Structures and Algorithms (Almost)), FIZRA (Physical Education), and QUALIFICATION_WORK.
 * These values can be used to categorize credit book records based on the subject of study.
 */
public enum Subject {
    OSI, OOP, DUITFCP, FIZRA, QUALIFICATION_WORK
}
